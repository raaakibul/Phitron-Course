#include<stdio.h>
int main(){
    int a = 10;
    int b = 5;
    printf("%d\n", a+b); // Addition
    printf("%d\n", a-b); // Subtraction
    printf("%d\n", a*b); // Multiplication
    printf("%d\n", a/b); // Division
    printf("%d\n", a%b); // Modulus
    printf("%d\n", a++); // Post-increment
    printf("%d\n", ++a); // Pre-increment
    printf("%d\n", a--); // Post-decrement
    printf("%d\n", --a); // Pre-decrement
}