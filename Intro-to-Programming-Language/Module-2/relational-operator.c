#include<stdio.h>
int main(){
    int a = 10;
    int b = 20;
    printf("%d\n", a==b); // Equal to
    printf("%d\n", a!=b); // Not equal to
    printf("%d\n", a>b);  // Greater than
    printf("%d\n", a<b);  // Less than
    printf("%d\n", a>=b); // Greater than or equal to
    printf("%d\n", a<=b); // Less than or equal to
    return 0;

}